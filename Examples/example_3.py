# a function that takes 2 integers as input and return their multiplication

def multiplication(x, y):
	result = x*y
	return result


 
# few examples

a = multiplication(5, 5)
print("5 * 5 = " + str(a))

b = multiplication(10, 3)
print("10 * 3 = " + str(b))

c = multiplication(723, 524)
print("723 * 524 = " + str(c))

