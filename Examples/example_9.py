# Example 9 (lambda)


# lambda creates anonymous functions
# that can be assigned to variables or as arguments
# or as I like to see it, a quick way to create a function

f1 = lambda z: z * 10
f2 = lambda z: z * 100

a = f1(10)
print(a)

b = f2(10)
print(b)
